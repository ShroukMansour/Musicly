class Song():
    def __init__(self, name, Band, featuredArtists, album,
                 releaseDate, genres, lyrics, length):
        self.name = name
        self.band = Band
        self.featuredArtists = featuredArtists
        self.album = album
        self.releaseDate = releaseDate
        self.genres = genres
        self.lyrics = lyrics
        self.length = length
